                if (nik == "") {
                    // alert('nik harus di isi');
                    Swal.fire({
                        title: 'OOoops!',
                        text: 'Nik Harus Di isi!',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        $("#nik").focus();
                    });

                    return false;
                } else if (nama_lengkap == "") {
                    Swal.fire({
                        title: 'OOoops!',
                        text: 'Nama Lengkap Harus Di isi!',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        $("#nama_lengkap").focus();
                    });

                    return false;
                } else if (jabatan == "") {
                    Swal.fire({
                        title: 'OOoops!',
                        text: 'Jabatan Harus Di isi!',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        $("#jabatan").focus();
                    });

                    return false;
                } else if (no_hp == "") {
                    Swal.fire({
                        title: 'OOoops!',
                        text: 'No. HP Harus Di isi!',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        $("#no_hp").focus();
                    });

                    return false;
                } else if (kode_dept == "") {
                    Swal.fire({
                        title: 'OOoops!',
                        text: 'Departemen Harus Di isi!',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        $("#kode_dept").focus();
                    });

                    return false;
                } else if (kode_cabang == "") {
                    Swal.fire({
                        title: 'OOoops!',
                        text: 'Cabang Harus Di isi!',
                        icon: 'warning',
                        confirmButtonText: 'OK'
                    }).then((result) => {
                        $("#kode_cabang").focus();
                    });

                    return false;
                }
            });

        });
